 function splitToChars(el) {
    const text = el.textContent.trim();
    el.textContent = '';
    for (let i = 0; i < text.length; i++) {
      const span = document.createElement('span');
      span.className = 'char';
      span.textContent = text[i] === ' ' ? '\u00A0' : text[i];
      span.style.transitionDelay = (i * 30) + 'ms';
      el.appendChild(span);
    }
  }

